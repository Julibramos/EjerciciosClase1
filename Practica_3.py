#Ingresar un numero del 1 al 7 y que indique que dia de la semana es
num= float(input("Ingresar numero del 1 al 7: "))
if num == 1:
    print("Lunes")
elif num ==2:
    print("Martes")
elif num ==3:
    print("Miercoles")
elif num ==4:
    print("Jueves")
elif num ==5:
    print("Viernes")
elif num ==6:
    print("Sabado")
elif num ==7:
    print("Domingo")